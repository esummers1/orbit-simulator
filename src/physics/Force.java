package physics;

public class Force {
    
    private double x;
    private double y;
    
    public Force(double x, double y) {
        this.x = x;
        this.y = y;
    }
    
    public double getX() {
        return x;
    }
    public void setX(double X) {
        this.x = X;
    }
    public double getY() {
        return y;
    }
    public void setY(double Y) {
        this.y = Y;
    }
    
}
